namespace Lab_02._SoftUni_Party
{
    using System;
    using System.Collections.Generic;

    class SoftUniParty
    {
        static void Main()
        {
            var vip = new HashSet<string>();
            var regular = new SortedSet<string>();

            while (true)
            {
                string input = Console.ReadLine();
                if (input == "PARTY")
                {
                    break;
                }
                if ()
                {
                    vip.Add(input);
                }
                else
                {
                    regular.Add(input);
                }
            }
        }
    }
}
